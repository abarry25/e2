    <h2> Instructions </h2>
    <ul>
        <li> Player A picks up both pigs. </li>
        <li> Player A rolls both pigs. </li>
        <li> Player A accumlates the total points gained by that roll. </li>
        <li> Points are scored based on how the pigs land. </li>
        <li> Player A continues to roll and gain points for each roll. </li>
        <li> Player A stops rolling when they either reach 100, or they lose all their points in a "Pig Out". </li>
        <li> If Player A reaches 100, they win! </li>
        <li> If Player A doesn't reach 100, the turn goes to Player B. </li>

        <li> Player B begins to roll and accumulate points. </li>
        <li> Player B stops rolling when they either reach 100, or they lose all their points in a "Pig Out" </li>
        <li> If Player B reaches 100, they win! </li>
        <li> If Player B doesn't reach 100, the turn goes to Player A </li>
        <li> The Players continue to go back and forth until someone reaches 100 in a single round of rolling </li>

